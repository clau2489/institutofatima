            <h1>Proyectos Nivel Primario 2020</h1>

          <br>
          <p>
            Al igual que hace 25 años, se  busca que los alumnos logren aprendizajes significativos a partir de los siguientes Proyectos<br>
            • Proyecto de merienda saludable desde el área de Ciencias Naturales y de Educación Física se desarrolla esta idea en donde se busca mejorar en la alimentación de los alumnos y que tomen conciencia de la importancia de la misma.<br>
            • Proyecto Lector: Desde todas las áreas se busca favorecer la lectura, la comprensión de textos y la importancia de la lectura y la escucha, por eso en este proyecto los más grandes de la Primaria leen a los más pequeños generando un intercambio de experiencias.<br>
            • Feria del Libro: La Primaria participa activamente de la Feria del Libro Institucional y además de la Feria Distrital.<br> 
            • Proyecto Crearte - Feria Distrital:  Participación activa en la Feria Distrital y de la Semana de las Artes<br>
            • Proyecto Gran Banda de Pequeños Sikuris: Este proyecto involucra no sólo a los Estudiantes, sino también a las familias<br> generando el aprendizaje de un instrumento y también la participación en fiestas y eventos municipales representando a la Institución buscando que el arte lleve los valores de la diversidad, el respeto y la tolerancia a todos lados.<br>
            • Salidas educativas: Investigar y experimentar forman parte de nuestros ideales en Educación, por eso proponemos que este tipo de jornadas sean colectivas y logremos aprendizajes significativos. <br>
            • Feria de Inglés: Este proyecto tiene como finalidad acentuar habilidades en los Estudiantes y que puedan producir de manera escrita y oral diversos textos.<br>
            • Proyectos desde la Educación Física: Campamentos y competencias intertribus de determinadas disciplinas. <br>
            • Proyecto de Educación Sexual Integral de acuerdo a la Ley N° 26150/06<br>
            • Proyectos Solidarios. Colaboración con múltiples Instituciones y Proyectos.<br>
